public class FinalKeyword {
    public static void main(String[] args) {
        /*
         * final Keyword
         * 
         * The value of a variable cannot be changed if the variable was declared using
         * the final keyword.
         * 
         * Note that the variable must be given a value when it is declared as final.
         * final variables cannot be changed; any attempts at doing so will result in an
         * error message.
         */

        // Value cannot be changed:
        final double PI = 3.14;
    }
}
