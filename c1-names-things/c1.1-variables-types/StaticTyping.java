public class StaticTyping {
    public static void main(String[] args) {
        /*
         * Static Typing
         * 
         * In Java, the type of a variable is checked at compile time. This is known as
         * static typing. It has the advantage of catching the errors at compile time
         * rather than at execution time.
         * 
         * Variables must be declared with the appropriate data type or the program will
         * not compile.
         */
        int i = 10; // type is int
        char ch = 'a'; // type is char

        j = 20; // won't compile, no type is given
        char name = "Lil"; // won't compile, wrong data type
    }
}
