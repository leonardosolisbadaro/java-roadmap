public class IncrementDecrementOperators {
    public static void main(String[] args) {
        /*
         * Increment and Decrement Operators
         * 
         * The increment operator, (++), can increase the value of a number-based
         * variable by 1 while the decrement operator, (--), can decrease the value of a
         * variable by 1.
         */
        int numApples = 5;
        numApples++; // Value is now 6

        int numOranges = 5;
        numOranges--; // Value is now 4
    }
}
